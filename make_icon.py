#!/usr/bin/env python3
"""Generate a pixel art PSP icon for PSP Sync."""

from PIL import Image

# 0=transparent, 1=#111, 2=#282828, 3=#555, 4=#AAA, 5=#FFF

PALETTE = {
    0: (0, 0, 0, 0),
    1: (0x11, 0x11, 0x11, 255),
    2: (0x28, 0x28, 0x28, 255),
    3: (0x55, 0x55, 0x55, 255),
    4: (0xAA, 0xAA, 0xAA, 255),
    5: (0xFF, 0xFF, 0xFF, 255),
}

ICON = [
    "00000000000000000000000000000000",
    "00000000000000000000000000000000",
    "00111111111111111111111111110000",
    "01222222222222222222222222210000",
    "01222222222222222222222222210000",
    "01223333333333322222222222210000",
    "01223444444443322222552222210000",
    "01223455555543322222222222210000",
    "01223455555543322225552222210000",
    "01223455555543322222222222210000",
    "01223444444443322222552222210000",
    "01223333333333322222222222210000",
    "01222222222222222222222222210000",
    "01222522222222222232322222210000",
    "01225552222232222323232222210000",
    "01222522222323222222322222210000",
    "01222222222232222222222222210000",
    "01222222222222222222222222210000",
    "01111111111111111111111111110000",
    "00000000000000000000000000000000",
    "00000005000000000000050000000000",
    "00000055000000000000055000000000",
    "00000555555555555000055500000000",
    "00000055000000000000055000000000",
    "00000005000000000555555555500000",
    "00000000000000000000550000000000",
    "00000000000000000000050000000000",
    "00000000000000000000000000000000",
    "00000000000000000000000000000000",
    "00000000000000000000000000000000",
    "00000000000000000000000000000000",
    "00000000000000000000000000000000",
]

def make_icon():
    img = Image.new("RGBA", (32, 32), (0, 0, 0, 0))
    for y, row in enumerate(ICON):
        for x, ch in enumerate(row):
            img.putpixel((x, y), PALETTE[int(ch)])

    img.save("icon_32.png")
    img.resize((256, 256), Image.NEAREST).save("icon.png")
    img.resize((512, 512), Image.NEAREST).save("icon_512.png")

    img16 = img.resize((16, 16), Image.NEAREST)
    img48 = img.resize((48, 48), Image.NEAREST)
    img256 = img.resize((256, 256), Image.NEAREST)
    img16.save("icon.ico", format="ICO", sizes=[(16, 16), (32, 32), (48, 48), (256, 256)],
               append_images=[img, img48, img256])

    print("Done")

if __name__ == "__main__":
    make_icon()
